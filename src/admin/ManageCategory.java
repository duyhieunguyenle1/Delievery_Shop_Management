    private boolean check() {
        String newCategory = nameCategory.getText();
        String oldCategory = model.getValueAt(rowIndex, 1).toString();

        if (newCategory.equals(oldCategory)) {
            return false;
        } else {
            boolean x = category.isCategoryExist(newCategory);
            if (x) {
                JOptionPane.showMessageDialog(null, "This category is already set", "Warning", 2);
            }
            return x;
        }
    }

