    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(String.valueOf(idCategory.getText()));
            String name = nameCategory.getText();
            String desc = descCategory.getText();
            

            if (!category.isCategoryExist(name)&&!category.isIDExist(id)) {
                category.insert(id, name, desc);
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Description"}));
                category.getCategoriesValue(jTable1, "");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Category name is already used", "Warning", 2);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(idCategory.getText());
            if (!category.isIDExist(id)) {
                if (!check()) {
                    String name = nameCategory.getText();
                    String desc = descCategory.getText();

                    category.updateCategory(id, name, desc);
                    jTable1.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Description"}));
                    category.getCategoriesValue(jTable1, "");
                    clear();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Category id doesnt' exist", "Warning", 2);
            }

            if (!check()) {
                String name = nameCategory.getText();
                String desc = descCategory.getText();

                category.updateCategory(id, name, desc);
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Description"}));
                category.getCategoriesValue(jTable1, "");
                clear();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(nameCategory.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please select a category", "Warning", 2);
        }else{
            int id = Integer.parseInt(idCategory.getText());
            if(category.isIDExist(id)){
                category.delete(id);
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Description"}));
                category.getCategoriesValue(jTable1, "");
                clear();
            }else{
                JOptionPane.showMessageDialog(this, "Category id doesnt' exist", "Warning", 2);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

